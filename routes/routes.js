const express = require('express');
const Signin = require('../controllers/loginController.js')
const Register = require('../controllers/registerController.js');
const  productsController= require('../controllers/productsController.js');
const verifyToken = require('../middleware/verifyToken.js');

const router = express.Router();


router.post('/login', Signin);
router.post('/register', Register);


router.post('/createProduct', verifyToken, productsController.createProduct);
router.get('/getProduct/:id', verifyToken, productsController.getProduct);
router.get('/getProducts', verifyToken, productsController.getProducts);
router.put('/editProduct/:id', verifyToken, productsController.updateProduct);
router.delete('/deleteProduct/:id', verifyToken, productsController.deleteProduct);

module.exports = router;






